int		ft_count_words(char *s)
{
	int i;
	int count;

	if (!s)
		return (0);
	i = 0;
	count = 0;
	while (s[i])
	{
		while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
			i++;
		if ((s[i] != ' ' || s[i] != '\n' || s[i] != '\t') && s[i] != '\0')
		{
			while ((s[i] != 32 || s[i] != '\n' || s[i] != '\t') && s[i] != '\0')
				i++;
			count++;
		}
	}
	return (count);
}
