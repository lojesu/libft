int		*ft_sort_tab(int *tab)
{
	int		i;
	int		j;
	int		x;

	i = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[j + 1] != '\0')
		{
			if (tab[j] > tab[j + 1])
			{
				x = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = x;
			}
			j++;
		}
		i++;
	}
	return (tab);
}
